x = 15
y = 4
print(x + y) #19
print(x - y) #11
print(x * y) #60
print(x / y) #3.75
print(x % y) #3
print(x ** y) #50625
print(x // y) #3

# OR AND XOR NOT
a = 12  # 1100 in binary
b = 5   # 0101 in binary
print(a | b)  # 13 (1101 in binary)
print(a & b)  # 4  (0100 in binary)
print(a ^ b)  # 9  (1001 in binary)
print(~a)     # -13 (inverts bits)
print(~b)     # -6  (inverts bits)

# Left Shift and Right Shift
c = 8  # 1000 in binary
print(c << 2) # 32 (100000 in binary)
print(c >> 2) # 2  (0010 in binary)